package com.logos.contacts.shared;

import java.io.Serializable;

import com.google.gwt.user.client.rpc.IsSerializable;

/**
 * 
 * Complex Fields Object to help communicate between server/client
 * 
 * @author cweber
 * 
 */
public class ComplexField implements Serializable, IsSerializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7214550810520303353L;
	private String type;
	private String value;

	public ComplexField() {
	}

	public ComplexField(String type, String value) {
		this.type = type;
		this.value = value;
	}

	public String getType() {
		return type;
	}

	public void setType(String label) {
		this.type = label;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}

}
